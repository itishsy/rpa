    public void loginOut() {
        try {
            Session session = SecurityContext.currentUser();
            if (session != null) {
                userSessionDao.remove(session.getUsername(), session.getSessionId());
            }
        }catch (Exception e) {

        }
    }

    private Map<String, Object> createTokenClaims(Integer userId, String userName, Integer custId) {
        Map<String, Object> claims = Maps.newHashMap();
        claims.put("jti", UUIDGenerator.uuidStringWithoutLine());
        claims.put("user_name", userName);
        claims.put("name", userName);
        claims.put("userId", userId);
        claims.put("custId", custId);
        claims.put("userType", 1);
        claims.put("authorities", Lists.newArrayList());
        Calendar calendar = Calendar.getInstance();
        calendar.add(Calendar.MONTH, 1);
        claims.put("exp", calendar.getTime());
        return claims;
    }
